suma_v1=int(input('Ingresa el primer valor de la suma: '))
suma_v2=int(input('Ingresa el segundo valor de la suma: '))
suma_r = suma_v1 + suma_v2
print("Resultado de la suma:", suma_r)
print()

resta_v1=int(input('Ingresa el primer valor de la resta: '))
resta_v2=int(input('Ingresa el segundo valor de la resta: '))
resta_r = resta_v1 - resta_v2
print('Resultado de la resta', resta_r)
print()

mul_v1=int(input('Ingresa el primer valor de la multiplicación: '))
mul_v2=int(input('Ingresa el segundo valor de la multiplicación: '))
mul_r = mul_v1 * mul_v2
print('Resultado de la multiplicación', mul_r)
print()

div_v1=int(input('Ingresa el primer valor de la división: '))
div_v2=int(input('Ingresa el segundo valor de la división: '))
div_r = div_v1 / div_v2
print('Resultado de la división', div_r)
print()

